        public override bool ChangePassword(string username, string oldPassword, string newPassword)
        {
            UserService context = new UserService();

            UserViewModel user = context.LoadByLoginId(username);

            if (Common.verifyMd5Hash(oldPassword, user.UserPass) == false)
            {
                return false;
            }
            user.UserPass = Common.getMd5Hash(newPassword);
            if (context.UpdateData(user) > 0)
            {

                return true;
            }
            else
            {
                return false;
            }
            
            
        }

